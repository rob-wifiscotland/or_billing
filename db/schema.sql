    adjustment_free_text_field varchar(255),
    charge_description_type varchar(20),
    charge_reason varchar(40),
    adjustment_date date,
    end_date date,
    address_line_1 varchar(80),
    postcode varchar(16),
    css_seibel_job_number varchar(20),
    sp_order_fault_number_1 varchar(20),
    sp_order_fault_number_2 varchar(20),
    quantity smallint,
    units varchar(40),
    unit_rate numeric(10,2),
    net_value numeric(10,2),
    vat_status smallint,
    css_account_number varchar(20),
    product_type varchar(20),
    or_service_id varchar(20),
    circuit_id varchar(20),
    mdf_site varchar(20),
    room_id varchar(20),
    service_id varchar(40),
    event_class varchar(20),
    event_name varchar(20),
    cbuk_reference_number varchar(20),
    cli varchar(20),
