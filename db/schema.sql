    price_list_reference varchar(256),
    price_list_description varchar(256)
);

CREATE TABLE IF NOT EXISTS openreach.billing_circuit_summary (
    circuit_summary_id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
    invoice_id uuid REFERENCES openreach.billing_invoices(invoice_id),
    circuit_number varchar(20),
    a_end_1141_code varchar(20),
    b_end_1141_code varchar(20),
    customer_order_number varchar(20),
    distance bigint,
    provide_order_date date,
    cease_date date,
    connection_charge numeric(10,2),
    bpr_days smallint,
    bpr_rental smallint,
    rental_charge numeric(10,2),
    credit_rental numeric(10,2),
    total_rental numeric(10,2),
    other_charges numeric(10,2),
    total_circuit_charges numeric(10,2)
);
